//===========================================================================//

function sun(word) {

    word = word.toLowerCase();
    var sub = null;

    // debugger;

    for (var i = 0; i < word.length; i ++) {

        if( word[i] === 's') {

            sub = word.substring( i, (i+3) );
        }

        if( sub === 'sun') {

            return true;

        }
    }

    return false;
}

